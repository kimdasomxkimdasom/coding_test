"""
문제: 해시 > 전화번호 목록
난이도: LEVEL 2
출처: 프로그래머스

[문제 설명]
전화번호부에 적힌 전화번호 중, 한 번호가 다른 번호의 접두어인 경우가 있는지 확인하려 합니다.

예시:
    구조대 : 119
    박준영 : 97674223
    지영석 : 1195524421
    → 119는 1195524421의 접두사이므로 False

전화번호부에 적힌 전화번호를 담은 배열 phone_book이 주어질 때,
어떤 번호가 다른 번호의 접두어인 경우가 있으면 False, 그렇지 않으면 True를 return.

[제한사항]
- phone_book의 길이는 1 이상 1,000,000 이하
- 각 전화번호의 길이는 1 이상 20 이하
- 같은 전화번호가 중복해서 들어있지 않음
"""


# ============================================================
# 풀이 1: 정렬 후 인접 비교
# ------------------------------------------------------------
# 핵심 아이디어:
#   문자열을 사전순으로 정렬하면, 접두어 관계에 있는 번호는
#   반드시 인접하게 배치된다. (예: "119" < "1195524421" < "12...")
#   따라서 정렬 후 바로 옆 번호만 비교하면 충분하다.
# 시간복잡도: O(n log n) (정렬)
# 공간복잡도: O(1) (제자리 정렬)
# ============================================================
def solution1(phone_book):
    phone_book.sort()

    # phon_book의 길이만큼 반복하되, i + 1이 범위를 벗어나지 않도록 len(phone_book) - 1까지
    for i in range(len(phone_book) - 1):
        # 현재 번호가 다음 번호의 접두어인지 확인 startswith() 메서드 사용
        if phone_book[i + 1].startswith(phone_book[i]):
            return False

    return True


# ============================================================
# 풀이 2: 해시맵(딕셔너리) 사용 ⭐ 출제 의도에 가장 부합
# ------------------------------------------------------------
# 핵심 아이디어:
#   모든 번호를 해시맵에 넣은 뒤, 각 번호를 한 글자씩 잘라가며
#   접두어가 해시맵에 존재하는지 확인한다.
#   → 해시 카테고리 문제이므로 이 접근이 출제 의도에 가장 부합
# 시간복잡도: O(n * m) (n: 번호 개수, m: 번호 최대 길이)
# 공간복잡도: O(n)
# ============================================================
def solution2(phone_book):
    # 모든 번호를 해시맵에 등록
    # 키 목록으로 딕셔너리를 만들면 조회가 O(1)로 빠르다.
    hash_map = dict.fromkeys(phone_book, True) # {'119': True, '97674223': True, '1195524421': True}

    for phone_number in phone_book:
        prefix = ""
        # 번호를 한 글자씩 붙여가며 접두어 검사
        for char in phone_number:
            prefix += char
            # 해당 접두어가 해시맵에 있고, 자기 자신이 아닐 때 → 접두어 관계 발견
            if prefix in hash_map and prefix != phone_number:
                return False

    return True


# ============================================================
# 풀이 3: set + 슬라이싱 활용
# ------------------------------------------------------------
# 핵심 아이디어:
#   풀이 2와 같은 원리지만, 문자열을 한 글자씩 붙이는 대신
#   슬라이싱(phone_number[:j])으로 접두어를 만든다.
#   set을 사용하여 조회 성능을 O(1)로 유지.
# 시간복잡도: O(n * m) / 공간복잡도: O(n)
# ============================================================
def solution3(phone_book):
    phone_set = set(phone_book)

    for phone_number in phone_book:
        # 길이 1부터 자기 자신 직전까지 모든 접두어를 검사
        for j in range(1, len(phone_number)):
            if phone_number[:j] in phone_set:
                return False

    return True


# ============================================================
# 테스트
# ============================================================
if __name__ == "__main__":
    test_cases = [
        (["119", "97674223", "1195524421"], False),   # 119가 1195524421의 접두어
        (["123", "456", "789"],             True),    # 접두어 관계 없음
        (["12", "123", "1235", "567", "88"], False),  # 12가 123의 접두어
        (["1", "2", "3", "4", "5"],         True),    # 한 자리 번호, 접두어 관계 없음
        (["9", "97", "976"],                False),   # 연쇄 접두어 (9→97→976)
    ]

    solutions = [
        ("풀이 1 (정렬)",      solution1),
        ("풀이 2 (해시맵)",    solution2),
        ("풀이 3 (set+슬라이싱)", solution3),
    ]

    for name, func in solutions:
        print(f"--- {name} ---")
        for phone_book, expected in test_cases:
            result = func(phone_book[:])  # 원본 보존을 위해 복사본 전달 (풀이1이 sort함)
            status = "PASS" if result == expected else "FAIL"
            print(f"  {status}: result={result}, expected={expected}")
        print()
